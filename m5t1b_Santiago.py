import turtle

def main():
    win = turtle.Screen()
    t = turtle.Turtle()

    #add some display options
    t.pensize(4)
    t.pencolor("blue")
    t.shape("turtle")

    #draw a "J"
    t.forward(60)
    t.right(90)
    t.forward(60)
    t.forward(60)
    t.right(90)
    t.forward(60)
    t.right(90)
    t.forward(30)
    
    #draw a "S"
    t.penup()
    t.forward(30)
    t.forward(60)
    t.right(90)
    t.forward(100)
    t.forward(100) 
    t.pendown()
    t.forward(60)
    t.left(180)
    t.forward(60)
    t.right(180)
    t.right(90)
    t.forward(60)
    t.left(90)
    t.forward(60)
    t.right(90)
    t.forward(60)
    t.right(90)
    t.forward(60)
    
    
    
    
    




    
    
main()
