import turtle             # Allows us to use turtles

def main():
    win = turtle.Screen()
    t = turtle.Turtle()

    #add some display options
    t.pensize(4)
    t.pencolor("red")
    t.shape("turtle")

    t.forward(50)
    t.left(90)
    t.forward(50)
    t.left(90)
    t.forward(50)
    t.left(90)
    t.forward(50)
    t.left(90)


    t.pencolor("blue")

    t.penup()
    t.forward(60)
    t.forward(30)
    t.pendown()
    t.forward(60)
    t.left(120)
    t.forward(60)
    t.left(120)
    t.forward(60)







main()    

    

    
    
